/* fazer um programa para imprimir
for é fácil 3 vezes
*/
#include <iostream>
using namespace std;

int main() {
    int z;
    for (z = 0; z < 3; z++){
        cout << "for e facil"<<endl;
    }
    // while
    z = 0;
    while ( z < 3 ){
        cout << "for e facil"<<endl;
        z++;// z = z + 1;
    }
    return 0;
}